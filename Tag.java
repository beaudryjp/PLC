public class Tag{

    private String v;
    private String f;

    public Tag(String a, String b){
        v = a;
        f = b;
    }

    public void setV(String a){
        v = a;
    }

    public void setF(String a){
        f = a;
    }

    public String getV(){
        return v;
    }

    public String getF(){
        return f;
    }

}